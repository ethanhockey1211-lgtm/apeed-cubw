/**
 * Postinstall patch: cubing.js ships the TNoodle three-phase 4x4 solver in a
 * lazy chunk but only exports random-scramble generation. Our 4x4 state
 * solver needs the internals (FullCube, Search, $doSearch, move tables), so
 * this script appends an export statement to the hashed chunk and writes a
 * stable-named shim next to it for imports.
 *
 * Idempotent; runs on every npm install (locally and on Render).
 */
import { readdirSync, readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(fileURLToPath(import.meta.url));
const chunksDir = join(root, "..", "node_modules", "cubing", "dist", "lib", "cubing", "chunks");
const MARKER = "/* cubeflow-444-exports */";
const EXPORTS = [
  "FullCube_3",
  "FullCube_4",
  "FullCube_5",
  "Search_4",
  "$doSearch",
  "move2str_1",
  "$move",
  "$move_0",
  "$move_1",
  "$move_2",
  "$move_3",
  "$move_4",
  "$move_5",
  "symmove",
  "symmult",
  "syminv",
  "move2rot",
  "getSolvedSym",
  "$getCenter",
];
// Expose the raw solution cube (move buffer + phase metadata) on the
// searcher so we can print the solution ourselves with explicit rotations —
// TNoodle's own printer substitutes opposite-face wide moves via implicit
// whole-cube rotations, which is fine for scrambles but wrong for solving.
const SOLCUBE_FROM = "this$static.solution = $getMoveString(solcube);";
const SOLCUBE_TO = "this$static.solcube = solcube; this$static.solution = $getMoveString(solcube);";

if (!existsSync(chunksDir)) {
  console.log("patch-cubing-444: cubing not installed, skipping");
  process.exit(0);
}
const chunk = readdirSync(chunksDir).find((f) => /^search-dynamic-solve-4x4x4-.*\.js$/.test(f));
if (!chunk) {
  console.error("patch-cubing-444: 4x4 solver chunk not found — cubing package layout changed");
  process.exit(1);
}
const chunkPath = join(chunksDir, chunk);
const src = readFileSync(chunkPath, "utf8");
if (!src.includes(MARKER)) {
  for (const name of EXPORTS) {
    if (!src.includes(`function ${name.replace("$", "\\$") && name}`) && !src.includes(`var ${name}`) && !src.includes(name)) {
      console.error(`patch-cubing-444: expected symbol ${name} missing from chunk`);
      process.exit(1);
    }
  }
  if (!src.includes(SOLCUBE_FROM)) {
    console.error("patch-cubing-444: solcube assignment not found — cubing package changed");
    process.exit(1);
  }
  const patched = `${src.replace(SOLCUBE_FROM, SOLCUBE_TO)}\n${MARKER}\nexport { ${EXPORTS.join(", ")} };\n`;
  writeFileSync(chunkPath, patched);
  console.log(`patch-cubing-444: patched ${chunk}`);
} else {
  console.log("patch-cubing-444: already patched");
}
// The cubing package's exports map forbids deep imports, so the shim lives in
// src/generated/ and reaches the chunk via a relative path (exempt from maps).
const genDir = join(root, "..", "src", "generated");
mkdirSync(genDir, { recursive: true });
writeFileSync(
  join(genDir, "solve444-internals.js"),
  `// Generated by scripts/patch-cubing-444.mjs — do not edit; gitignored.\nexport * from "../../node_modules/cubing/dist/lib/cubing/chunks/${chunk}";\n`,
);
console.log("patch-cubing-444: shim written (src/generated/solve444-internals.js)");
